router.get("/api/workouts", (req, res) => {
  Work.aggregate( [
    {
       $addFields: {
          "totalDuration": {$sum: "$exercises.duration"},
       },
    },
] )
    .then(dbWorkout => {
      res.json(dbWorkout);
    })
    .catch(err => {
      res.status(400).json(err);
    });
});

module.exports = router;
