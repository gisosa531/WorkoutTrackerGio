const router = require('express').Router();
const Work = require('../models/Workout.js');

router.post("/api/workouts", ({ body }, res) => {
  Work.create(body)
    .then(dbWorkout => {
      res.json(dbWorkout);
    })
    .catch(err => {
      res.status(400).json(err);
    });
});

